    private void bentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bentrarActionPerformed
        // TODO add your handling code here:
        try
        {
            if(bdd.entrarLogin(tuser.getText(), tpass.getText()))
            {
                if(bdd.getTipoUsuario(tuser.getText()).equals("administrador"))
                {
                    jfadmin admin = new jfadmin();
                    admin.setVisible(true);
                }
                else if(bdd.getTipoUsuario(tuser.getText()).equals("administrador"))
                {
                    jfcomprador c = new jfcomprador();
                    c.setVisible(true);
                }
                tuser.setText("");
                tpass.setText("");
                this.hide();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o contrase√±a incorrectos.");
            }
            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Error de programa");
        }
    }//GEN-LAST:event_bentrarActionPerformed

